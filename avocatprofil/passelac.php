<p>Avocat au Barreau de Paris depuis 1998, Anne Charlotte conseille quotidiennement ses clients – dirigeants d’entreprises, cadres et institutions sociales, français comme étrangers – sur l’ensemble des problématiques de droit social et de la protection sociale qu’ils rencontrent, qu’elles s’inscrivent dans un cadre individuel ou collectif (mise en place des institutions représentatives du personnel, élaboration de procédures internes, négociations d’accords collectifs). </p>
    <p>La parfaite connaissance de ses clients et de leurs activités permet à Anne Charlotte de leur apporter des solutions concrètes, tenant compte de l’environnement juridique, des contraintes opérationnelles et de la stratégie de l’entreprise dans un cadre législatif et réglementaire complexe et en mouvement constant. </p>
    <p>Anne Charlotte assiste ses clients tant en conseil qu’en contentieux et précontentieux. Elle a par ailleurs développé une expertise tout à fait particulière en restructuration sociale. Sa compréhension des problématiques sociales des entreprises en difficulté fait d’Anne Charlotte le partenaire privilégié des chefs d’entreprises et administrateurs judiciaires qu’elle accompagne notamment dans le cadre de la mise en œuvre de plans de sauvegarde pour l’emploi, des négociations avec les institutions représentatives du personnel, de leurs relations avec l’inspection du travail et la DIRRECTE.</p>
    <p> Après 20 ans au sein d’un même Cabinet d’affaires, Anne Charlotte a cofondé JAMES AVOCATS, un cabinet à son image, proche de ses clients, disponible et réactif, concevant pour eux des solutions innovantes et rationnelles.</p>
