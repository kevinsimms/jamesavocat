<h2 id="titreavocat2" class="profilname">Anne-Laure LAVERGNE</h2>
    <p>Anne-Laure est l’un des Associés fondateurs de JAMES AVOCATS.</p>
<p>Avocat au Barreau de Paris depuis 2011, Anne-Laure a développé une expertise et un savoir-faire spécifiques dans les domaines relevant du droit immobilier, droit des contrats et droit social. </p>
    <p>Présentant une double compétence en droit des affaires et en économie, elle conseille quotidiennement depuis plus de 10 ans les entreprises, associations, fondations, institutions ainsi que leurs dirigeants, dans la gestion de leurs projets et leur stratégie, en s’efforçant de proposer des solutions innovantes et pragmatiques.</p>
    <p>Forte de ses 10 années passées au sein d’un Cabinet d’avocats Conseil en entreprise et de son expérience acquise au sein d’un cabinet d’expertise-comptable et d’une étude d’administrateur judiciaire - qui lui permet d’appréhender les enjeux comptables et financiers des opérations envisagées – Anne Laure accompagne les entreprises dans leurs projets immobiliers, de construction ou commerciaux, et les assiste dans le cadre de la résolution précontentieuse ou contentieuse de leurs différends dans ces matières.</p>
<p>Elle conseille également ces mêmes acteurs dans la gestion de leurs personnels, et la résolution de leurs conflits.</p>
<p>Anne-Laure anime, enfin, des formations dans ces mêmes domaines d’intervention.</p>
