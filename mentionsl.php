<h1>Politique de gestion <br>des cookies</h1>


<p>1. Déclaration relative à l’utilisation de cookies</p>

<p>Lors de la consultation de notre site disponible à l’adresse https://www.james-avocats.fr (ci-après le « Site ») des cookies sont déposés sur l’ordinateur, la tablette ou tout terminal utilisé par vous (ci-après l’« Utilisateur »). Ceci nous permet d’analyser la fréquentation des pages et d’améliorer le Site. Les cookies sont utilisés selon leur nature et aux fins ci-après indiquées.</p>

<p>L’Utilisateur a la possibilité d’accepter ou de refuser les cookies en utilisant le mécanisme intégré au bandeau qui apparaitra lors de sa première visite sur le Site.</p>

<p>2. Qu’est-ce qu’un Cookie ?</p>

<p>Un cookie (ci-après le(s) « Cookie(s) « ) est un fichier texte déposé sur un terminal lors de la visite du Site ou de la consultation d’une publicité. Un Cookie a notamment pour but de collecter des informations relatives à la navigation de l’Utilisateur sur le Site et d’adresser à l’Utilisateur des services et contenus personnalisés.</p>
  
<p>3. Identification des Cookies </p>


<p>Il existe différents types de Cookies :<br> Cookies tiers et Cookies propriétaires<br> Un Cookie est dit « propriétaire » ou « tiers » selon le domaine dont il émane. Les Cookies propriétaires sont ceux installés par le Site lorsque l’Utilisateur est en train de le consulter. Les Cookies tiers sont des Cookies installés par un domaine autre que celui du Site (à titre d’exemple, un cookie peut être déposé par une régie publicitaire tierce sur le Site). Lorsqu’un Utilisateur consulte le Site et qu’une autre entité installe un Cookie par l’intermédiaire du Site, ce Cookie est un Cookie tiers. Cookies de session<br> Ces Cookies permettent aux exploitants du Site de suivre les actions d’un Utilisateur le temps d’une session de navigation. La session de navigation commence dès que l’Utilisateur ouvre la fenêtre du navigateur et finit lorsqu’il ferme cette fenêtre. Les Cookies de session ont un caractère temporaire. Une fois le navigateur fermé, tous les Cookies de session sont supprimés. <br>Cookies persistants<br> Ces Cookies restent sur l’appareil de l’Utilisateur après la fermeture de la session de son navigateur et ce, pour la durée prévue par chacun de ces Cookies. Ils sont activés chaque fois que l’Utilisateur consulte le Site.</p>

<p>4. Quels types de Cookies l’Editeur utilise et pour quelles finalités ?  </p>

<p>Différents types de Cookies sont utilisés sur le Site, ils ont des finalités différentes. Certains sont nécessaires pour utiliser le Site, d’autres sont optionnels selon vos choix. Les Cookies techniques strictement nécessaires Ces Cookies sont nécessaires au fonctionnement du Site. Ils permettent à l’Utilisateur d’utiliser les principales fonctionnalités du Site. Sans ces Cookies, l’Utilisateur ne peut pas utiliser le Site normalement. Ces Cookies requièrent uniquement une information de l’Utilisateur pour être déposés sur le terminal de l’Utilisateur et n’autorisent aucun suivi comportemental ou d’identification. Les Cookies fonctionnels de personnalisation Ces Cookies permettent de personnaliser l’expérience de l’Utilisateur sur le Site en mémorisant ses préférences et permettent notamment : d’adapter la présentation du Site aux préférences d’affichage du terminal (langue utilisée, résolution d’affichage, système d’exploitation utilisé, etc…) lors des visites des Utilisateurs sur le Site, selon les matériels et les navigateurs ; de mémoriser des informations relatives aux formulaires que l’Utilisateur a remplis sur le Site ou à des informations que l’Utilisateur a choisies sur le Site. Les Cookies de mesure d’audience Les Cookies de mesure d’audience aident à établir des statistiques de fréquentation et d’utilisation des divers éléments composant le Site (rubriques et contenus visités, parcours). Nous utilisons notamment les cookies Google Analytics. </p>

<p>Les informations stockées via ces cookies ne permettent pas d’identifier personnellement les internautes, et sont directement transférées à Google, sans que nous n’en ayons connaissance. Pour en savoir plus sur ces cookies, vous pouvez consulter la politique de protection des données de Google Analytics disponible à l’adresse https://www.google.com/analytics/learn/privacy.html?hl=fr.</p>

<p>5. Comment l’Utilisateur peut-il gérer les Cookies ?</p>

<p> Lors de sa première connexion, l’Utilisateur est informé que JAMES AVOCATS utilise des cookies. Seul l’émetteur d’un Cookie est susceptible de lire des informations qui y sont contenues et l’Editeur n’a aucun accès sur les Cookies que des tiers peuvent être amenés à utiliser. L’émission et l’utilisation de Cookies par des tiers sont soumises aux politiques de confidentialité de ces tiers en plus des dispositions de la présente Politique. En conséquence l’Utilisateur est invité à se rendre sur les sites Internet de ces tiers pour plus d’informations sur les Cookies qu’ils enregistrent et comment l’Utilisateur peut les gérer. </p>

<p>La gestion par l’Utilisateur du dépôt des Cookies </p>

<p>L’Utilisateur peut gérer le dépôt des Cookies de la manière suivante :</p>

<p> Le dépôt des Cookies techniques strictement nécessaires est activé par défaut et ne peut être désactivé par l’Utilisateur, ces Cookies étant indispensables au fonctionnement du Site ; </p>

<p>L’Utilisateur désactive ou active l’implantation des Cookies de mesure d’audience via le bandeau d’information qui sera visible lors de sa première visite sur le Site. Toute désactivation entraine le refus par l’Utilisateur du dépôt des Cookies en question. En poursuivant sa navigation, c'est-à-dire lorsque l'Utilisateur a cliqué sur un élément du Site (image, lien, bouton « rechercher » etc.) ou s'est rendu sur une autre page du Site, l’Utilisateur valide l’implantation des Cookies, selon le paramétrage par défaut proposé. Si l'Utilisateur active l'enregistrement de Cookies dans son terminal, les Cookies intégrés dans les pages et contenus qu'il a consultés pourront être stockés temporairement dans un espace dédié de son terminal. Ils y seront lisibles uniquement par leur émetteur. Le consentement donné par l'Utilisateur n'est valable que pour une durée de treize (13) mois à compter du premier dépôt dans l'équipement du terminal de l'Utilisateur, faisant suite à l'expression du consentement de ce dernier.</p>

<p>Si l'Utilisateur désactive l'enregistrement de Cookies dans son terminal ou son navigateur, ou s'il supprime ceux qui y sont enregistrés, l'Utilisateur est informé que sa navigation et son expérience sur le Site pourraient être différentes des autres Utilisateurs ayant activé les Cookies (contenus non personnalisés). Cela pourrait également être le cas lorsque JAMES AVOCATS ou l’un de nos prestataires ne pourrait pas reconnaître, à des fins de compatibilité technique, le type de navigateur utilisé par le terminal, les paramètres de langue et d'affichage ou le pays depuis lequel le terminal semble connecté à Internet. Le cas échéant, JAMES AVOCATS décline toute responsabilité pour les conséquences liées au fonctionnement dégradé du Site résultant du refus de Cookies par l'Utilisateur. </p>

<p>Pour en savoir plus</p>

<p>L’Utilisateur peut se rendre sur le site internet de la CNIL afin de recueillir des informations concernant les cookies via notamment la page suivante : https://www.cnil.fr/fr/site-web-cookies-et-autres-traceurs.</p>

